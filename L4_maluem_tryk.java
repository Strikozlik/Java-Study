public class L4_maluem_tryk {
    public static void main(String [] args){
        for(int i=0;i<10;i++){                              //тут робимо рядки
            int zirka=10-i;                                  //тут визначаэмо кількість зірочок в рядку
             for(int j=0;j<zirka;j++){                           // цикл для зірочок
                 System.out.print("*");                     // виводимо в одному рядку певну кількість зірочок

             }System.out.println();                           //переводим в  новий рядок зірочки
        }


    }
}
